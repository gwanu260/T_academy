/**
 * 변수
 * - 문법
 *  - 변수 선언
 *      키워드 변수명
 *  - 변수 선언 및 초기화
 *      키워드 변수명 = 값
 * - 용도
 *  - 값을 담는 그릇(초보적 해석)
 *  - 값을 가르키는 `참조`값을 가진 그릇(값을 가르킨다)
 * 
 * - 변수 키워드(<-> 파이썬에는 없음)
 *  - (*)var
 *      - 초기때부터 사용
 *      - 변수 선언, 범위에 대한 버그가 존재함 -> 변수명이 안 겹치면 일단 해결됨.
 *  - 추가됨 (2015, ES6)
 *      - 변수의 범위를 블록 스코프( { 여기서만 유효 } )로 구성
 *      - let       :
 *          - 변수 키워드, 범위에 대한 버그 해결함.
 *      - (*)const  : 상수 키워드 (<-> 파이썬 없음)
 *          - 변수명은 대문자구성(관습) -> 상수
 *          - (*)함수/객체를 참조할 때 사용, 환경변수, 설정값
 */

