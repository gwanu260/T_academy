// 2. 변수
// 스코프 : 변수의 적용 범위 (전역변수, 지역변수 => {}, 블럭이존재함)
// JS는 파이썬과 동일하게 타입 추론형(값에 세팅되면 타입이 결정된다)임
var a = 1; // 전역변수,
console.log( a, typeof a); // 값출력, 변수의 타입조사 (number로 나옴:수치타입)

a = "hello"; // 기존 변수에 다른 타입의 값 부여
console.log( a, typeof a); // 값을 참조하므로 문제 없음 (잘 사용 X)

a = 1;
if(true) { // 코드블럭, 현재 조건은 무조건 진입
    var a = 10; // 변수 a를 정의했지만, 이미 전역에 a가 var로 정의되어 있어서 같은 변수
                // 이름을 다르게 사용하면 문제 x
    var b = 11; // 맥락상 지역변수 이지만, {} 밖에서도 사용 가능함.
    let c = 12; // 스코프 기반하여 사용범위를 제한 -> 찐 로컬 변수
}

console.log( a );
console.log( b );
